# Generate JS
lines = ['const CARD_DATA = {']
for card_id, langs in CARD_DATA.items():
    lines.append(f"  '{card_id}':" + '{')
    for lang, d in langs.items():
        cat = d['cat'].replace("'", "\\'")
        q = d['q'].replace("'", "\\'")
        lines.append(f"    {lang}:" + "{" + f"cat:'{cat}',q:'{q}'" + "},")
    lines.append('  },')
lines.append('};')
CARD_DATA_JS = '\n'.join(lines)

# Step 3: Insert CARD_DATA before applyLanguage function
marker = '  function applyLanguage(lang) {'
content = content.replace(marker, CARD_DATA_JS + '\n\n' + marker, 1)

# Step 4: Extend applyLanguage() to update cards
old_end = '    document.documentElement.lang = lang;\n    replaceWithFSymbol();\n  }'
new_end = '''    // Update market card text
    document.querySelectorAll('[data-card-id]').forEach(function(card) {
      var id = card.dataset.cardId;
      var d = CARD_DATA[id];
      if (!d) return;
      var ld = d[lang] || d['ko'];
      if (ld) {
        var catEl = card.querySelector('.market-category');
        var qEl = card.querySelector('.market-question');
        if (catEl) catEl.textContent = ld.cat;
        if (qEl) qEl.textContent = ld.q;
      }
    });
    document.documentElement.lang = lang;
    replaceWithFSymbol();
  }'''
content = content.replace(old_end, new_end, 1)

with open('main.html', 'w', encoding='utf-8') as f:
    f.write(content)
